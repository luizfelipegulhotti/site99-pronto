            <h2>Quem foi Santo Estêvão</h2>
            <p>Santo Estêvão foi o primeiro mártir (protomártir) da Igreja Católica, conforme relatado nos Atos dos Apóstolos. Seu nome significa "coroa" ou "coroado" em grego, simbolizando a recompensa celestial que recebeu por seu testemunho de fé.</p>
            
            <p>Foi um dos sete diáconos escolhidos pelos apóstolos para ajudar na distribuição de alimentos e assistência aos necessitados na comunidade cristã primitiva. Conhecido por sua fé ardente, sabedoria e eloqüência, realizou grandes prodígios e sinais entre o povo.</p>
            
            <p>Estêvão foi falsamente acusado de blasfêmia contra Moisés e contra Deus. Durante seu julgamento perante o Sinédrio, defendeu corajosamente sua fé e teve uma visão de Jesus à direita de Deus. Isto enfureceu seus acusadores, que o arrastaram para fora da cidade e o apedrejaram até a morte. Seu último ato foi pedir perdão para seus assassinos, seguindo o exemplo de Cristo.</p>
            
            <p>Seu martírio ocorreu por volta do ano 34 d.C., e é comemorado em 26 de dezembro, logo após o Natal.</p>
